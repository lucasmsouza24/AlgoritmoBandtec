// SSJ
function ssj() {
    var ki_base = Number(input_ki_base.value);
    var ki = ki_base * 50;
    display_text.innerHTML = `Após se transformar em SSJ, o KI desse Sayajin passa a ser de ${ki}.`;
    display_img.src = "img/ssj.png";
}

